 <strong>Mercedes</strong><br><br>

 Mercedes (oficjalnie od 2017 roku Mercedes AMG Petronas Motorsport) – fabryczny zespół wyścigowy Mercedes-Benz w Formule 1, reaktywowany w 2009 po wykupieniu 75,1% udziałów w Brawn GP.<br>
 Szefem zespołu od stycznia 2014 roku jest Toto Wolff.<br><br>
 <a id="historia_text"></a>
 <strong>Historia</strong><br><br>

 Koncern Mercedes-Benz zadebiutował w Formule 1 w sezonie 1954 i uczestniczył w mistrzostwach przez dwa lata.<br><br>
 Wówczas, w barwach zespołu Mercedesa dwa spośród swoich pięciu tytułów mistrzowskich wywalczył Argentyńczyk Juan Manuel Fangio.<br><br>
 Pomimo sukcesów, niemiecki koncern zdecydował się jednak opuścić F1 z końcem sezonu 1955, kiedy to z powodu tragedii na torze Le Mans podczas wyścigu 24-godzinnego, w której zginęły 83 osoby (w tym jeden kierowca Pierre Levegh) Mercedes wycofał się ze sportów motorowych.<br>
 Następnie marka Mercedes pojawiała się w mistrzostwach w latach 90. oraz w pierwszej dekadzie XXI wieku jako dostawca silników.<br>
 Początkowo, w 1994 roku Mercedes dostarczał silniki zespołowi Sauber, jednak od kolejnego sezonu pracował dla McLarena.<br><br>

 16 listopada 2009 roku Mercedes oficjalnie ogłosił wykupienie większościowego pakietu 75,1% udziałów w zespole Brawn GP i przekształcił go na swój zespół fabryczny – Mercedes Grand Prix.<br>
 Było to możliwe dzięki umowie pomiędzy koncernem Daimler-Benz AG (który został właścicielem 45,1% akcji zespołu) oraz jego partnerem, firmie Aabar Investments (która nabyła pozostałe 30% akcji).<br>
 Szefem zespołu pozostał jednak twórca zespołu Brawn GP – Ross Brawn.<br><br>

 21 grudnia 2009 zespół podpisał wieloletnią umowę sponsorską z malezyjskim koncernem paliwowym Petronas, wskutek której zespół wystartował w sezonie 2010 jako Mercedes GP Petronas Formula One Team.<br><br>

 Pierwszymi kierowcami po reaktywacji zespołu został Nico Rosberg oraz siedmiokrotny mistrz świata, Michael Schumacher najbardziej utytułowany kierowca w historii Formuły 1.<br><br>
 <a id="team_changes_text"></a>
 Do zmian personalnych w kierownictwie zespołu doszło w 2014 roku. Po odejściu Rossa Brawna rolę dyrektora zespołu współdzielą Paddy Lowe i Toto Wolff.<br><br>

 W latach 2013–2016 kierowcami Mercedesa byli Lewis Hamilton i Nico Rosberg. W sezonie 2017 kierowcami Mercedesa są Lewis Hamilton i Valtteri Bottas.<br><br>

 W latach 2014–2016 zespół całkowicie zdominował rywalizację, zdobywając tytuły mistrzowskie w klasyfikacji konstruktorów. Lewis Hamilton sięgnął po tytuł w pierwszych dwóch sezonach, natomiast Nico Rosberg w ostatnim z nich.<br><br>

